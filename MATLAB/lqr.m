M = 4; % 车身质量kg
m = 0.5; % 轮子质量kg

I = 4; % 车身转动惯量kg*m^2
i = 0.5; % 轮子转动惯量kg*m^2

h = 0.4; % 车质心高度m
r = 0.1; % 轮子半径m

g = 9.8; % 重力加速度m/s^2
%-----------------------------------------------------------------------%

% 临时常数
Z = M + m + i / (r * r);
D = Z * (I + M * h * h) - M * M * h * h;



a = (M * g * h * Z) / D;

b = -(M * M * h * h * g) / D;

c = -(M * h) / (D * r);

d = 1 / (Z * r) - (M * M * h * h) / (D * Z * r);

%-----------------------------------------------------------------------%

A = [0, 1, 0, 0;
     a, 0, 0, 0;
     0, 0, 0, 1;
     b, 0, 0, 0];

B = [0;
     c;
     0;
     d];


Q = [1;
     1;
     1;
     1];




